import java.lang.*;
import java.util.*;
public class Arithmetic_Operators {

public static void main(String args[])
{

    int i=4;
    byte b=2;
    float f=2.5f;
    char c ='A';
    short s = 9;
    long l = 10;
    double d = 8.5d;


    System.out.println("Byte+Short:\t"+ b+s);
    System.out.println("Short+Integer:\t"+ s+i);
    System.out.println("Integer+Float:\t"+ i+f);
    System.out.println("long+float:\t"+ l+f);
    System.out.println("character+short:\t"+ c+s);
    System.out.println("Character+integer:\t"+ c+i);
    System.out.println("Float+Double:\t"+ f+d);
    System.out.println("long+double:\t"+ l+d);

}

}
