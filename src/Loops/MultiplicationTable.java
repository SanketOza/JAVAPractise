package Loops;
import java.util.*;
import java.lang.*;
public class MultiplicationTable {

    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int n;
        System.out.println("Enter Number : ");
        n = sc.nextInt();


        for (int i = 1; i <= 10; i++) {


            System.out.println(n+"*"+i+"="+n*i);


        }
    }






}
